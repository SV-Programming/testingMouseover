 * Setup Instructions
To ensure this works correctly in Unity 6, follow these three steps:

Add a Collider: Your 2D object must have a BoxCollider2D, CircleCollider2D, or similar.

Add a Physics 2D Raycaster: *Select your Main Camera.

Click Add Component and search for Physics 2D Raycaster. This allows the mouse to "see" 2D objects.

Ensure an Event System exists: Right - click in your Hierarchy -> UI -> Event System. You need this in your scene for any "Pointer" events to work.

Build Settings: Ensure any scene you want to load is added to the Scenes in Build list (File > Build Settings).

    Don't forget to add the scenes to the build settings.








